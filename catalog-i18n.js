function tc(key, fallback) {
  const lang = getLanguage();
  if (lang === "it") return fallback;
  const table = CATALOG_MESSAGES[lang] || {};
  return table[key] ?? fallback;
}

function getFoodName(food) {
  if (!food) return "";
  if (typeof food === "string") {
    const item = typeof getFoodById === "function" ? getFoodById(food) : null;
    return item ? tc(`food.${item.id}`, item.name) : food;
  }
  return tc(`food.${food.id}`, food.name);
}

function getActivityName(activity) {
  if (!activity) return "";
  const id = typeof activity === "string" ? activity : activity.id;
  const fallback = typeof activity === "string" ? activity : activity.name;
  return tc(`activity.${id}`, fallback);
}

function getFoodCategoryLabel(category) {
  return tc(`category.${category}`, FOOD_CATEGORY_LABELS?.[category] || category);
}

function getActivityCategoryLabel(category) {
  const fallback = category === "lavoro" ? "Lavoro" : category === "sport" ? "Sport" : category;
  return tc(`activityCat.${category}`, fallback);
}

function getMealDisplayName(meal) {
  if (!meal) return "";
  if (meal.foodId) {
    const food = typeof getFoodById === "function" ? getFoodById(meal.foodId) : null;
    if (food) return getFoodName(food);
  }
  return meal.name || "";
}

function getActivityDisplayName(activity) {
  if (!activity) return "";
  if (activity.activityId) {
    const preset = typeof getActivityById === "function" ? getActivityById(activity.activityId) : null;
    if (preset) return getActivityName(preset);
  }
  return activity.name || "";
}
