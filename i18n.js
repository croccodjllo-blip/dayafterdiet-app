function daysLabel(count) {
  if (getLanguage() === "en") return count === 1 ? t("common.day") : t("common.days");
  if (getLanguage() === "es") return count === 1 ? "día" : "días";
  return count === 1 ? "giorno" : "giorni";
}

let currentLanguage = DEFAULT_LANGUAGE;

function normalizeLanguage(value) {
  const lang = String(value || "").slice(0, 2).toLowerCase();
  return SUPPORTED_LANGUAGES.includes(lang) ? lang : DEFAULT_LANGUAGE;
}

function getLanguage() {
  return currentLanguage;
}

function getLocaleTag() {
  return LOCALE_TAGS[getLanguage()] || LOCALE_TAGS[DEFAULT_LANGUAGE];
}

function formatLocaleNumber(value, options) {
  return Number(value).toLocaleString(getLocaleTag(), options);
}

function t(key, params = {}) {
  const lang = getLanguage();
  const table = MESSAGES[lang] || MESSAGES[DEFAULT_LANGUAGE];
  const fallback = MESSAGES[DEFAULT_LANGUAGE];
  let text = table[key] ?? fallback[key] ?? key;
  for (const [name, value] of Object.entries(params)) {
    text = text.replace(new RegExp(`\\{${name}\\}`, "g"), String(value));
  }
  return text;
}

function getWeekdayName(dayIndex, style = "long") {
  const date = new Date(2024, 0, 7 + dayIndex);
  return date.toLocaleDateString(getLocaleTag(), { weekday: style });
}

function getBodyMeasureLabel(key) {
  return t(`body.${key}`);
}

function getMealLabel(key) {
  return t(`meal.${key}`);
}

function getMicroLabel(key) {
  return t(`micro.${key}`);
}

function getActivityLevelLabel(key) {
  return t(`activityLevel.${key}`);
}

function getDietObjectiveLabel(key, full = false) {
  if (full) {
    const fullKey = `${key}Full`;
    const fullLabel = t(`diet.${fullKey}`);
    if (fullLabel !== `diet.${fullKey}`) return fullLabel;
  }
  return t(`diet.${key}`);
}

function getIntensityLabel(key) {
  return t(`intensity.${key}`);
}

function initLanguage(preferred) {
  const stored = localStorage.getItem(LOCALE_KEY);
  currentLanguage = normalizeLanguage(preferred ?? stored ?? DEFAULT_LANGUAGE);
  localStorage.setItem(LOCALE_KEY, currentLanguage);
  document.documentElement.lang = currentLanguage;
}

function applyPageTranslations() {
  document.documentElement.lang = getLanguage();

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });

  document.querySelectorAll("[data-i18n-title]").forEach((el) => {
    el.title = t(el.dataset.i18nTitle);
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    el.setAttribute("aria-label", t(el.dataset.i18nAria));
  });

  const pageTitleKey = document.body?.dataset?.i18nPageTitle;
  if (pageTitleKey) {
    document.title = `DaD — ${t(pageTitleKey)}`;
  }

  const pageHeading = document.querySelector("[data-i18n-page-heading]");
  if (pageHeading) pageHeading.textContent = t(pageHeading.dataset.i18nPageHeading);

  translateSelectOptions(document.getElementById("diet-objective"), {
    maintain: "diet.maintainFull",
    lose_light: "diet.lose_lightFull",
    lose: "diet.loseFull",
    gain: "diet.gainFull",
  });
  translateSelectOptions(document.getElementById("edit-diet-objective"), {
    maintain: "diet.maintain",
    lose_light: "diet.lose_light",
    lose: "diet.lose",
    gain: "diet.gain",
  });
  translateSelectOptions(document.getElementById("edit-activity"), {
    sedentary: "activityLevel.sedentary",
    light: "activityLevel.light",
    moderate: "activityLevel.moderate",
    active: "activityLevel.active",
    very: "activityLevel.very",
  });
  translateSelectOptions(document.getElementById("edit-sex"), {
    m: "M",
    f: "F",
  });
  translateSelectOptions(document.getElementById("reg-sex"), {
    m: "M",
    f: "F",
  });
  translateSelectOptions(document.getElementById("reg-activity"), {
    sedentary: "activityLevel.sedentary",
    light: "activityLevel.light",
    moderate: "activityLevel.moderate",
    active: "activityLevel.active",
    very: "activityLevel.very",
  });
  translateSelectOptions(document.getElementById("reg-diet-objective"), {
    maintain: "diet.maintain",
    lose_light: "diet.lose_light",
    lose: "diet.lose",
    gain: "diet.gain",
  });
}

function translateSelectOptions(select, map) {
  if (!select) return;
  for (const option of select.options) {
    const key = map[option.value];
    if (key) option.textContent = key.startsWith("diet.") || key.startsWith("activityLevel.") ? t(key) : key;
  }
}
